# This program multiply two number provided by user

# Get two numbers
num1 = int(input('Enter first number: '))
num2 = int(input('Enter second number: '))

# Multiply these two numbers
num3 = float(num1 * num2)

# Print result
print('Multiplication of {0} and {1} is {2}' . format(num1, num2, num3))